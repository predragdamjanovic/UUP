/*
APSTRAKTNA (nat)KLASA
klasa koja ne moze imati svoje objekte
(nije moguce instancirati je)
 */
public abstract class Oblik {

    /*
    apstraktni metod koji nema definiciju i
    klasa koja ga sadrzi takodje mora biti apstraktna
     */
    public abstract void crtaj();

    public abstract void povrsina();
}
